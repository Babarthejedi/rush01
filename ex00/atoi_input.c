#include <stdio.h>

int	*atoi_input(char *str, int tab[16])
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '1' && str[i] <= '4')
			tab[j++] = str[i] - 48;
		i++;
	}
	return (tab);
}
